#include "JoystickDriver.c"

void init() {

}

task arm() {
	while(true) {
			getJoystickSettings(joystick);
			if(joy1Btn(6)){
				motor[scoop] = 40;
			}
			else if(joy1Btn(8 )){
				motor[scoop] = -40;
			}
			else{
				motor[scoop] = 0;
			}
			if(joy2Btn(2)){
				servo[bucketTilt] = 255;
			}
			else if(joy2Btn(4)){
				servo[bucketTilt] = 0;
			}
			else{
				servo[bucketTilt] = 127;
			}
			if(joy2Btn(5)){
				motor[tilt] = 100;
			}
			else if(joy2Btn(7)){
				motor[tilt] = -100;
			}
			else{
				motor[tilt] = 0;
			}
			if(joy2Btn(6)){
				motor[liftMotors] = -100;
			}
			else if(joy2Btn(8)){
				motor[liftMotors] = 100;
			}
			else{
				motor[liftMotors] = 0;
			}
			if(joy1Btn(6)){
				servo[grabber] = 20;
			}
			else if(joy1Btn(8)){
				servo[grabber] = 140;
			}
	}
}

task main()
{
	waitForStart();
	StartTask(arm);
	while(true) {
		getJoystickSettings(joystick);

		if(abs(joystick.joy1_y1) < 10)
			joystick.joy1_y1 = 0;
		if(abs(joystick.joy1_y2) < 10)
			joystick.joy1_y2 = 0;

		motor[leftMotors] = joystick.joy1_y1 * -100.0/128;
		motor[rightMotors] = joystick.joy1_y2 * -100.0/128;
	}
}
