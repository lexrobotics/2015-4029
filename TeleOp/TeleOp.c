#include "JoystickDriver.c"

void init() {
	servo[bucketTilt] = 20;
	//servo[ballGate] = 255;
	//servo[grabber] = 127;
}

int bucketliftval = 20;
task arm() {
	while(true) {
			getJoystickSettings(joystick);
			if(joy1Btn(6)){
				motor[spinner] = -100;
			}
			else if(joy1Btn(8)){
				motor[spinner] = 100;
			}
			else{
				motor[spinner] = 0;
			}

			if(joy2Btn(2)){
				bucketliftval++;
				if(bucketliftval > 255)
					bucketliftval = 255;
			}
			else if(joy2Btn(4)){
				bucketliftval--;
				if (bucketliftval<20)
					bucketliftval = 20;
			}
			if(joy2Btn(6)){
				motor[liftStageTwo] = -100;
			}
			else if(joy2Btn(8)){
				motor[liftStageTwo] = 100;
			}
			else{
				motor[liftStageTwo] = 0;
			}

			if(joy2Btn(3)){
				servo[bucketGate] = 70;
			}
			else{
				servo[bucketGate] = 255;
			}

			if(joy2Btn(5)){
				motor[liftStageOne] = 100;
			}
			else if(joy2Btn(7)){
				motor[liftStageOne] = -100;
			}
			else{
				motor[liftStageOne] = 0;
			}

			if(joy1Btn(5)){
				servo[grabber] = 0;
			}
			else if(joy1Btn(7)){
				servo[grabber] = 255;
			}
			else {
				servo[grabber] = 127;
			}
			servo[bucketTilt] = bucketliftval;
	}
}

task main()
{
	waitForStart();
	StartTask(arm);
	while(true) {
		getJoystickSettings(joystick);

		if(abs(joystick.joy1_y1) < 10)
			joystick.joy1_y1 = 0;
		if(abs(joystick.joy1_y2) < 10)
			joystick.joy1_y2 = 0;

		motor[leftMotors] = joystick.joy1_y1 * -100.0/128;
		motor[rightMotors] = joystick.joy1_y2 * -100.0/128;
	}
}
