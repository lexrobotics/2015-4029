	//parrallell to wall
	turnUltra(0);
	pause(0.5);
	parallel(30);
	pause(0.5);
	turnUltra(90);
	tillBack(-50,false, 60);
	move(0);
	//Use ultrasonics to calculate angle needed to reach second tube
	float d1 = SensorValue[backUltra] + 21.6 - 30.0;
	float d2 = SensorValue[frontUltra] + 17.8 - 20.0;
